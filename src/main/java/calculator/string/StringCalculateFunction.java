package calculator.string;

public class StringCalculateFunction {
    public static Long plus(Long v1, Long v2) {
        return v1 + v2;
    }

    public static long minus(Long v1, Long v2) {
        return v1 - v2;
    }

    public static long multiply(Long v1, Long v2) {
        return v1 * v2;
    }

    public static long divide(Long v1, Long v2) {
        return v1 / v2;
    }
}
