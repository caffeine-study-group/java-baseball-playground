package calculator;

public class CalculateFunctionImpl {
    public Long plus(Long v1, Long v2) {
        return v1 + v2;
    }

    public long minus(Long v1, Long v2) {
        return v1 - v2;
    }

    public long multiply(Long v1, Long v2) {
        return v1 * v2;
    }

    public long divide(Long v1, Long v2) {
        return v1 / v2;
    }
}
